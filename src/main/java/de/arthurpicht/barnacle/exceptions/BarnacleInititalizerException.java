package de.arthurpicht.barnacle.exceptions;

public class BarnacleInititalizerException extends BarnacleRuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1374885303554361297L;

	public BarnacleInititalizerException() {
		super();
	}
	
	public BarnacleInititalizerException(Throwable cause) {
		super(cause);
	}
	
	public BarnacleInititalizerException(String s) {
		super(s);
	}
	
	public BarnacleInititalizerException(String s, Throwable cause) {
		super(s, cause);
	}
}
