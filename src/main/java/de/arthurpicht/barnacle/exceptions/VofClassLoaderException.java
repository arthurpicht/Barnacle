package de.arthurpicht.barnacle.exceptions;

public class VofClassLoaderException extends BarnacleException {


	/**
	 * 
	 */
	private static final long serialVersionUID = 5087729430171233439L;

	public VofClassLoaderException() {
		super();
	}
	
	public VofClassLoaderException(Throwable cause) {
		super(cause);
	}
	
	public VofClassLoaderException(String s) {
		super(s);
	}
	
	public VofClassLoaderException(String s, Throwable cause) {
		super(s, cause);
	}
}
