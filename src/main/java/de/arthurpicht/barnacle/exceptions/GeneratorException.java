package de.arthurpicht.barnacle.exceptions;

public class GeneratorException extends BarnacleException {


	/**
	 * 
	 */
	private static final long serialVersionUID = 7286506474703547393L;

	public GeneratorException() {
		super();
	}
	
	public GeneratorException(Throwable cause) {
		super(cause);
	}
	
	public GeneratorException(String s) {
		super(s);
	}
	
	public GeneratorException(String s, Throwable cause) {
		super(s, cause);
	}
	
	
}
